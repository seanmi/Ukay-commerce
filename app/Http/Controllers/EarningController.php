    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $total = 0;
        $orders = Order::where('date_delivered', '!=', NULL)->get();

        foreach ($orders as $order) {
            $total += number_format(round($order->orderItems->sum('total_price')+$order->shipping_fee,1) - round($order->orderItems->sum('takehome'),1),2);
        }
      

        return view('admin.earning.index')
            ->with('orders', Order::where('date_delivered', '!=', NULL)->get())
            ->with('path','/admin/earning/')
            ->with('title', 'Earnings')
            ->with('total', $total);
    }

